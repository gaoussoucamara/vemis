package com.vemis.model;

public class HypotheseModeleDeSimulation {
	private int numero;
	private String nomHypothese;
	private String natureHypothese;
	private String elementDHypothese;
	private String quantificationHypothese;
	
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	public String getNomHypothese() {
		return nomHypothese;
	}
	public void setNomHypothese(String nomHypothese) {
		this.nomHypothese = nomHypothese;
	}
	public String getNatureHypothese() {
		return natureHypothese;
	}
	public void setNatureHypothese(String natureHypothese) {
		this.natureHypothese = natureHypothese;
	}
	public String getElementDHypothese() {
		return elementDHypothese;
	}
	public void setElementDHypothese(String elementDHypothese) {
		this.elementDHypothese = elementDHypothese;
	}
	public String getQuantificationHypothese() {
		return quantificationHypothese;
	}
	public void setQuantificationHypothese(String quantificationHypothese) {
		this.quantificationHypothese = quantificationHypothese;
	}
	
}
