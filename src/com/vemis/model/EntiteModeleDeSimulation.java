package com.vemis.model;


public class EntiteModeleDeSimulation {
	private int numeroEntite;
	private String typeEntite;
	private String nomEntite;
	public int getNumeroEntite() {
		return numeroEntite;
	}
	public void setNumeroEntite(int numeroEntite) {
		this.numeroEntite = numeroEntite;
	}
	public String getTypeEntite() {
		return typeEntite;
	}
	public void setTypeEntite(String typeEntite) {
		this.typeEntite = typeEntite;
	}
	public String getNomEntite() {
		return nomEntite;
	}
	public void setNomEntite(String nomEntite) {
		this.nomEntite = nomEntite;
	}
	
	
}
