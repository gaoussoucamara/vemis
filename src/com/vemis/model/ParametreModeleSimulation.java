package com.vemis.model;

public class ParametreModeleSimulation {
	private int numeroParametre;
	private String nomParametre;
	private String typeParametre;
	private String elementParametre;
	private String quantificationParametre;
	
	
	public int getNumeroParametre() {
		return numeroParametre;
	}
	public void setNumeroParametre(int numeroParametre) {
		this.numeroParametre = numeroParametre;
	}
	public String getNomParametre() {
		return nomParametre;
	}
	public void setNomParametre(String nomParametre) {
		this.nomParametre = nomParametre;
	}
	public String getTypeParametre() {
		return typeParametre;
	}
	public void setTypeParametre(String typeParametre) {
		this.typeParametre = typeParametre;
	}
	public String getElementParametre() {
		return elementParametre;
	}
	public void setElementParametre(String elementParametre) {
		this.elementParametre = elementParametre;
	}
	public String getQuantificationParametre() {
		return quantificationParametre;
	}
	public void setQuantificationParametre(String quantificationParametre) {
		this.quantificationParametre = quantificationParametre;
	}
	
}
